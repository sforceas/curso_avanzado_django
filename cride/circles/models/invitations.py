"""Invitation model."""

# Django
from django.db import models
from django.db.models.deletion import CASCADE, SET_NULL
from cride.circles.managers.invitations import InvitationManager

# Utilities
from cride.utils.models import CRideModel

class Invitation(CRideModel):
    """Invitation model.
    
    A circle invitation is a random text that acts
    as unique code that grants access to a specific 
    circle.This codes are generated by users that 
    are already members of the circle and 
    have 'remaining_invitations' value greater than 0
    """

    code = models.CharField(max_length=50,unique=True)
    issued_by = models.ForeignKey(
        'users.User',
        on_delete=models.CASCADE,
        help_text='Circle member that is providing the invitation',
        related_name='issued_by'
    )
    used_by = models.ForeignKey(
        'users.User',
        on_delete=models.CASCADE,
        null=True,
        help_text='User that is recieving the invitation',
        related_name='used_by'
    )
    circle = models.ForeignKey('circles.Circle',on_delete=models.CASCADE)

    used = models.BooleanField(default=False)
    used_at = models.DateTimeField(blank=True,null=True)

   # Manager
    objects  = InvitationManager()

    def __str__(self):
        """Return circle and code"""
        return '#{}:{}'.format(self.circle,self.code)
    